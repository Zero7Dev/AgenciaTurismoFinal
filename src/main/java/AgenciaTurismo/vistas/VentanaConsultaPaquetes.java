    private void btnBuscarParametroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarParametroActionPerformed
        switch (this.jComboBox1.getSelectedItem().toString()) {
            case "--OPCIONES--":
                this.lblTituloBusqueda.setText("");
                this.cmbTipoViaje.setVisible(false);
                this.btnBuscarComun.setVisible(false);
                this.btnBuscarUnico.setVisible(false);
                this.txtPaqueteBuscado.setVisible(false);
                break;
            case "DATO UNICO":
                this.lblTituloBusqueda.setText("INGRESE EL NOMBRE PAQUETE TURISTICO: ");
                btnBuscarUnico.setVisible(true);
                this.txtPaqueteBuscado.setVisible(true);

                this.cmbTipoViaje.setVisible(false);
                this.btnBuscarComun.setVisible(false);
                this.txtInformacion.setVisible(true);
                this.jPanelComun.setVisible(false);

                break;
            case "DATO COMUN":
                this.lblTituloBusqueda.setText("SELECCIONE EL TIPO DE VIAJE: ");

                this.cmbTipoViaje.setVisible(true);
                this.btnBuscarComun.setVisible(true);

                this.data_Table.setVisible(true);

                this.btnBuscarUnico.setVisible(false);
                this.txtPaqueteBuscado.setVisible(false);
                this.txtInformacion.setVisible(false);
                this.jPanelComun.setVisible(true);
                this.jpanelInfoUnico.setVisible(false);

                break;
            default:
                throw new AssertionError();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarParametroActionPerformed

