    PaqueteTuristicoDAO paqueteDAO = new PaqueteTuristicoDAO();
ArrayList<PaqueteTuristicoDTO> listaComun=new ArrayList<>();
    public void cargarTabla(String datoComun) {
        listaComun.clear();
         listaComun= paqueteDAO.BusquedaTipoViaje(datoComun);
        System.out.println(listaComun.toString());
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        String[] columnas = {"ID", "NOMBRE", "DESTINO", "TIPOVIAJE", "PRECIO"};
        modelo.setColumnIdentifiers(columnas);
        for (PaqueteTuristicoDTO paquete : listaComun) {
            Object[] paqueteTuristico = new Object[]{paquete.getId(), paquete.getNombrePaquete(),
                paquete.getDestino(), paquete.getTipoViaje(), paquete.getPrecioFinal()};
            modelo.addRow(paqueteTuristico);
        }
        data_Table.setModel(modelo);
    }

    public void cargarInfoDatoUnico(String nombrePaquete) {
        PaqueteTuristicoDTO paquete = paqueteDAO.buscarXnombre(nombrePaquete);
        if (paquete == null) {
            txtInformacion.setText("EL PAQUETE NO SE ENCUENTRA REGUISTRADO");
            return;
        }
        txtInformacion.setText(paquete.MostrarDatos());
    }

