    public void DatosTabla(String datos){
    ArrayList<HospedajeDTO> listaHab = hospDAO.buscarXHabitacion(Integer.parseInt(datos));
    System.out.println(listaHab.toString());
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
    String[] columnas = {"NOMBRE", "APELLIDO","# HABITACIONES", "PRECIO"};
    modelo.setColumnIdentifiers(columnas);
    for(HospedajeDTO hospe : listaHab){ // Only iterate through the filtered list
        Object[] hospedaje = new Object[]{hospe.getNombre(), hospe.getapellido(), hospe.getNumeroHabitaciones(), hospe.getprecioFinal()};
        modelo.addRow(hospedaje);
    }
    Tbregistro.setModel(modelo);
}
    
    public void informacion(String nombre){
        HospedajeDTO hospedaje = hospDAO.buscarXNombre(nombre);
        if (hospedaje == null) {
            txtInformacion.setText("no hay nadie registrado con ese nombre");
            return;
        }
        txtInformacion.setText(hospedaje.toString());
    }
