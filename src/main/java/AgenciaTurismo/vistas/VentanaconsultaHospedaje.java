    private void bttbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttbuscarActionPerformed
        // TODO add your handling code here:
        switch (this.CmbBusquedad.getSelectedItem().toString()) {
            case "SELECCION":
            this.lbTitulo.setText("");
            this.cmbNumeroHabitaciones.setVisible(false);
            this.bttNhabitaciones.setVisible(false);
            this.txtNombre.setVisible(false);
            this.bttNombre.setVisible(false);
            break;
            case "NOMBRE":
            this.lbTitulo.setText("Ingrese su nombre: ");
            this.bttNombre.setVisible(true);
            this.txtNombre.setVisible(true);

            this.cmbNumeroHabitaciones.setVisible(false);
            this.bttNhabitaciones.setVisible(false);
            this.txtInformacion.setVisible(true);
            this.JPanelTable.setVisible(false);

            break;
            case "# HABITACIONES":
            this.lbTitulo.setText("Seleccione el numero de habitaciones: ");

            this.cmbNumeroHabitaciones.setVisible(true);
            this.bttNhabitaciones.setVisible(true);

            this.Tbregistro.setVisible(true);

            this.bttNombre.setVisible(false);
            this.txtNombre.setVisible(false);
            this.txtInformacion.setVisible(false);
            this.JPanelTable.setVisible(true);
            this.JPinfo.setVisible(false);

            break;
            default:
            throw new AssertionError();
        }

    }//GEN-LAST:event_bttbuscarActionPerformed

