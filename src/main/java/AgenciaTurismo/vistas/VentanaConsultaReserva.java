       public void cargarTabla(int datoComun) {
        ArrayList<ReservaDTO> listaComun = AccesodatosReserva.BusquedaDia(datoComun);
        System.out.println(listaComun.toString());
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        String[] columnas = {"ID", "CLIENTE", "PAQUETE"};
        modelo.setColumnIdentifiers(columnas);
        for (ReservaDTO reserva : listaComun) {
            Object[] reservaRow = new Object[]{reserva.getIdReserva(), reserva.getCliente().getCedula(),
                reserva.getPaqueteTuristico().getNombrePaquete()};
            modelo.addRow(reservaRow);
        }
        TablaReservas.setModel(modelo);
    }

       
