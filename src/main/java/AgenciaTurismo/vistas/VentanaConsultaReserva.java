    private void BotonSearchIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonSearchIdActionPerformed
        // TODO add your handling code here:
        reserva = AccesodatosReserva.BusquedaID(this.txtBusquedaUnica.getText());
        if (reserva == null) {
            this.TxtInformacionUnico.setText("No hay ningun cliente reguistrado con esa cedula");
            return;
        }
        this.TxtInformacionUnico.setText(reserva.toString());
    }//GEN-LAST:event_BotonSearchIdActionPerformed

